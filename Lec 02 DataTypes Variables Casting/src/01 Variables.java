class Variables {
    public static void main(String[] args) {
        int myNumber = 865;
        System.out.println(myNumber);

        boolean isIt = true;
        System.out.println(isIt);

        float myFloat = 5.0f;
        System.out.println(myFloat);

        String wishes = "Good Morning!";
        System.out.println(wishes);
    }
}