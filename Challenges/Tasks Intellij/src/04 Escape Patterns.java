class Escape {
    public static void main(String[] args) {
        // Pattern 01 - Right Half Pyramid
        System.out.println("* \n* * \n* * * \n* * * * \n* * * * *");

        System.out.println();

        // Reverse Right Pyramid
        System.out.println("* * * * * \n* * * * \n* * * \n* * \n* ");

        System.out.println();

        // Left Half Pyramid
        System.out.println("        * \n      * *\n    * * * \n  * * * *\n* * * * *");
    }
}
